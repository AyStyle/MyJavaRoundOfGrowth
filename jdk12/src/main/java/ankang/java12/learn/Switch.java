public class Switch {

    public static void main(String[] args) {
        int x = 0;

        switch (x) {
            case 1 -> System.out.println(x);
            case 2 -> System.out.println(x);
            case 3 -> System.out.println(x);
            default -> System.out.println(x);
        }
    }

}
