public class Stream {
    /*
     * 流（Stream）：保存的是对集合或数组数据的操作。和集合类似，但集合中保存的是数据。
     * 特点：
     *      1.流自己不会存储元素
     *      2.流不会改变源对象
     *      3.流会返回一个持有结果对象的新Stream
     *      4.流的操作是延迟执行的，等需要结果的时候才会执行
     *
     * 使用：
     *      1.创建流 - 新建一个流
     *      2.中间操作 - 在一个或多个步骤中，将初始的Stream转化为另一个Stream的操作
     *      3.终止操作 - 使用终止操作来产生结果对象，该操作会强制它之前的延迟操作立即执行。在这之后，该Stream就不能使用了
     *  */
}
